#include <iostream>

using namespace std;

int main()
{
  //en este programa vamos a crear un bucle, es decir, un codigo que sirve para que algo se repita el numero
  //de veces que necesitemos.
  
  int i; //variable de tipo entero.
  
  //el for esta dividido en tres partes. La primera parte indica desde donde comenzamos. La segunda parte
  //nos va a indicar hasta donde tenemos que llegar. En nuestro caso, llegaremos mientras i<10. La tercera 
  //parte nos indica que el valor de i se a ir incrementando en 1. Cada una de las tres partes esta separada
  //por punto y coma ";".
  for(i=0;i<10;i++)
  {
                   cout<<"ESTA ES UNA PRUEBA"<<endl; //con cout imprimimos el valor de i. Endl sirve para imprimir el siguiente valor en la siguiente linea.
   
   }    
  cin.get(); //esta linea genera una pausa al final del programa.  
    
    
}
